IG.util.getRandomInt = function(min, max) {'use strict';
	return Math.floor(Math.random() * (max - min + 1)) + min;
};

/**
 * Translate name to specific index, e.g. 
 * "Sonne" -> 0
 * The collection can be found at : IG.util.spaceObjects.collection
 * Time complexity in worst case: O(n), average case O(n/2), best case: O(1)
 */
IG.util.translateToCollection = function(spaceObjectName){
	'use strict';
	var obj,
	collection = IG.spaceObjects.collection;
	
	//loop over the collection and compare the name 
	//of the object in current iteration with the 
	//spaceObjectName defined as parameter (argument).
	for(obj in collection){
		if(collection.hasOwnProperty(obj)){
				if(collection[obj].nameOfTheSpaceObject === spaceObjectName){
					//After that get the index and return it. 
					return collection[obj];		
				}
		} 
	}
};

/**
 * Get x and y coordinate of the given space object.
 * @param {Object} spaceObject
 */
IG.util.getPlanetPosition = function(spaceObject){
	'use strict';
	return {
		xCoordinate: IG.util.translateToCollection(spaceObject).xCoordinate,
		yCoordinate :  IG.util.translateToCollection(spaceObject).yCoordinate
	};

	
};


IG.util.createRandomPath = function(startObject, endObject, result) {
			'use strict';
			//Variable declaration (JSLint suggestion)
			var path, initialPosition, numberOfDummySegments, counter, segment, endPosition, buffer, coin,
			startXcoordinate = IG.util.getPlanetPosition(startObject).xCoordinate,
			startYcoordinate =  IG.util.getPlanetPosition(startObject).yCoordinate,
			endXcoordinate =  IG.util.getPlanetPosition(endObject).xCoordinate + IG.util.getRandomInt(-2, 2),
			endYcoordinate =  IG.util.getPlanetPosition(endObject).yCoordinate;
			
			
			if(endObject === 'Erde'){
				coin = Math.random();
				if(coin < 0.5){
					endYcoordinate = endYcoordinate + IG.util.getRandomInt(1, 30);
				} else {
					endYcoordinate = endYcoordinate - IG.util.getRandomInt(1, 40);
				}
				
			}
			
			
			buffer = 30;
			
			//initialize an empty array
			path = [];

			//define start coordinates
			initialPosition = {
				'x' : startXcoordinate + 3,
				'y' : startYcoordinate + 3
			};
			//add the initial position to the path array
			path.push(initialPosition);

			// the number of segments in the line interpolation (between 1 and 3)
			numberOfDummySegments = IG.util.getRandomInt(1, 2);

			//create a loop, which generates dummy segments in a given range
			counter = 0;
			while (counter < numberOfDummySegments) {
				//define the range
				segment = {
					'x' : IG.util.getRandomInt(Math.min(startXcoordinate + buffer,
							endXcoordinate + buffer), Math.max(startXcoordinate + buffer, 
							endXcoordinate + buffer)),
					'y' : IG.util.getRandomInt(Math.min(startYcoordinate + buffer, endYcoordinate + buffer), 
					Math.max(startYcoordinate + buffer, endYcoordinate + buffer))
				};

				path.push(segment);

				counter = counter + 1;
			}
			//add the end position to close the path
			endPosition = {
				'x' : endXcoordinate + 1,
				'y' : endYcoordinate + 1
			};

			path.push(endPosition);

			return path;
		};

